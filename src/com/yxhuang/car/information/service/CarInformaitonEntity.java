package com.yxhuang.car.information.service;

public class CarInformaitonEntity {
	private String mCarTitle;
	private String mCarImagerUrl;
	private String mCarTime;
	private int mCarComment;
	
	public String getCarTitle() {
		return mCarTitle;
	}
	public void setCarTitle(String carTitle) {
		mCarTitle = carTitle;
	}
	public String getCarImagerUrl() {
		return mCarImagerUrl;
	}
	public void setCarImagerUrl(String carImagerUrl) {
		mCarImagerUrl = carImagerUrl;
	}
	public String getCarTime() {
		return mCarTime;
	}
	public void setCarTime(String carTime) {
		mCarTime = carTime;
	}
	public int getCarComment() {
		return mCarComment;
	}
	public void setCarComment(int carComment) {
		mCarComment = carComment;
	}
	
	

}
